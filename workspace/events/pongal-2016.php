           <p class="methodText"><strong>January 15th, 2016 - Miracle City & Miracle Heights IND : </strong> Pongal is the Harvest Festival of India. This festival is called as the Four-Day long harvest festival particularly in the Southern India. Pongal is celebrated in various forms in different parts of the country. It is celebrated as “Sankranti” in Andhra Pradesh and Telangana, “Thai Pongal” in Tamil Nadu and also as “Uttarayana” in some parts of India. The four-day festival signifies traditional thanksgiving to nature after harvesting the crops.</p>
          <p class="methodText">
According to Hindu mythology Pongal is one of the most ancient festivals celebrated between 200 AD to 300 AD (The Sangam Age) particularly in South India. Pongal was generally originated as the Dravidian Harvest Festival as mentioned in the Sanskrit Puranas. In the earlier days people had a belief that this festival season welcomed the spring season for the agricultural community. In the present days, Sankranti is celebrated for four days as Bhogi, Makara Sankranthi, Kanuma and Mukkanuma.</p><br>
