         <p class="methodText">The first day of Sankranti is <strong>Bhogi</strong>, people light up bonfires with logs of wood, old furniture and other old belongings that are not necessary. It represents the cleansing of all sins and the start of new beginnings. The second day of sankranti is <strong>Makara Sankranti</strong>, people offer prayers and traditional food to their ancestors. Third day is celebrated as <strong>Kanuma</strong> which represents reunion of  entire family and shows cultural values. The last day, <strong>Mukkanuma</strong> is the day on which farmers offer prayers to elements which helped in harvesting.
</p>
