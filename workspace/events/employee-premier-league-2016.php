                     <h5 class="heavy">Tourney Rules</h5>
                    <p class="methodText">
                        <ul>
                            <li>Each team needs a minimum of 13 players and maximum of 16 players</li>
                            <li>Matches will be played from September 17th, 2016 to November 19th, 2016</li>
                            <li>Team will be asked to play consecutive matches sometimes without rest(Will be avoided as much as possible)</li>
                            <li>All umpire decisions will be final and cannot be argued</li>
                            <li>There will be no LBWs and Leg Byes</li>
                            <li>Wides will be given 1 Run and the ball will be re-bowled</li>
                            <li>For a No Ball(Foot Crease) 1 run will be given along with a free hit and an extra ball</li>
                            <li>Above the waist full toss and above the head regular balls will be considered as No-Balls</li>
                            <li>Maximum of 2 fielders can be there behind Square Leg</li>
                            <li>In case of rain the match will be continued if the first innings is completed, else will be cancelled (or) postponed based on possibilities and replayed from the start</li>
                            <li><strong>Points System :</strong> Win(2), Draw(1 each), Abandoned(1 each) and Lost(0) <ul>
                            <li>In case of points being tied teams will be compared with run-rate</li>    
                            </ul>
                            </li>
                            
                            <li><strong>Awards :</strong> MoM Medals for all matches, Special MoM for Eliminators and Semi-Finals, Best Batsman/Bowler and MVP</li>
                        </ul>
                        </p>
